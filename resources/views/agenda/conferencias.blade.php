    <tr>
        <td>Resumen</td>
        <td>La computadora es una lasaña muy alta, capa de abstracción sobre capa de abstracción. Usualmente, en los enfoques curriculares para CC de nivel inicial, primario y secundario, las capas bajas, cómo el hardware y el sistema operativo se dejan de lado por prejuicios respecto a la dificultad. El mismo está presente también en la educación superior en CC. El conferencista tratará de argumentar que las capas bajas, por un lado, contienen todas las ideas, conceptos y métodos que definen a la Computación como una Ciencia, y por otro, que al ser más concretas al punto que se pueden tocar, bajan un poco el salto de abstracción que las y los estudiantes tienen que hacer, y con esto tal vez se mejora la transferencia de los contenidos y la aceptación por parte de ellas y ellos. Se espera un ida y vuelta de la concurrencia para debatir estas ideas.</td>
